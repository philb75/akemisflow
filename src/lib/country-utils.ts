/**
 * Convert a 2-letter country code to full country name
 * @param countryCode - 2-letter ISO country code (e.g., 'MA', 'TN', 'AE')
 * @returns Full country name or the original code if not found
 */
export function getCountryName(countryCode?: string): string {
  if (!countryCode) return '-'
  
  const fullName = COUNTRY_CODE_MAP[countryCode.toUpperCase()]
  return fullName || countryCode
}

/**
 * Get country info object with both code and name
 * @param countryCode - 2-letter ISO country code
 * @returns Object with code and name
 */
export function getCountryInfo(countryCode?: string): { code: string; name: string } {
  if (!countryCode) return { code: '-', name: '-' }
  
  const code = countryCode.toUpperCase()
  const name = COUNTRY_CODE_MAP[code] || code
  
  return { code, name }
}